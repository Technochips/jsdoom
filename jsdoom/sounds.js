
class sfxinfo_t
{
	name = null;
	singularity = 0;
	priority = 0;
	link = null;
	pitch = 0;
	volume = 0;
	data = null;
	usefulness = 0;
	lumpnum = 0;
	constructor(name, singularity, priority, link, pitch, volume, data, usefulness, lumpnum)
	{
		this.name = name;
		this.singularity = singularity;
		this.priority = priority;
		this.link = link;
		this.pitch = pitch;
		this.volume = volume;
		this.data = data;
		this.usefulness = usefulness;
		this.lumpnum = lumpnum;
	}
};

